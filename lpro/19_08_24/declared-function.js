// function declaration
// declarared named function
function multi(a, b) {
  return r = a * b;
  return r;
}

let res = multi(2, 3);
console.log(res);

let n1 = 5;
let n2 = 10;
resultado = multi(n1, n2);
console.log(`${n1} X ${n2} = ${resultado}`); //interpolação
console.log(n1 + " X " + n2 + " = " + resultado); //concatenação